question = input("Ask a question: ")

if "ai" in question.lower():
    print("AI Assistant: AI means Artificial Intelligence. It helps computers think and learn.")

elif "automation" in question.lower():
    print("AI Assistant: Automation means using technology to perform tasks automatically.")

elif "hello" in question.lower():
    print("AI Assistant: Hello! I am your first AI assistant.")

else:
    print("AI Assistant: I am still learning. Ask me something about AI or automation.")